# This simple calculator adds 2 numbers

# Example 1: whole numbers

num1 = input("Enter a number: ")
num2 = input("Enter another number: ")
result = int(num1) + int(num2)
# int() converts the number into whole number integers.

print(result)

# Example 2: decimals

num1 = input("Enter a number: ")
num2 = input("Enter another number: ")
result = float(num1) + float(num2)
# float() allows working with decimal places

print(result)
